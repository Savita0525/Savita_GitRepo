package day1.bacis;

public class AreaOfCircle {

	public static void main(String[] args) {
		System.out.println("program start");
		final float PI;
		PI= 3.14f;
		double r;
		r = 2.5;
		float Area;
		Area = (float) (PI*r*r);
		System.out.println("area of circle"+Area);
		System.out.println("program ends");
	     
		
		

	}

}
